namespace FitApp.Models;

// Тип подхода — как в Hevy.
// Хранится в SQLite как int. Не меняй значения, иначе старые записи поедут.
public enum SetType
{
    Normal = 0,   // Обычный — нумеруется 1, 2, 3...
    Warmup = 1,   // Разминка — бейдж "W", оранжевый
    Failure = 2,  // В отказ — бейдж "F", красный
    DropSet = 3   // Дроп-сет — бейдж "D", синий
}
